    Lock lock = new ReentrantLock();

    public MyH2DataSource(String url, String username, String password) {
        this.url = url;
        this.username = username;
        this.password = password;
    }

    @Override
    public ConnectionWrapper getConnection() {
        try {
            lock.lock();
            if (connection == null) {
                connection = new ConnectionWrapper( DriverManager.getConnection(
                        url,
                        username,
                        password));
            }
            while (!connection.isNotInUse) ;
            connection.isNotInUse = false;
            lock.unlock();
            return connection;
        } catch (SQLException e) {
            throw new RuntimeException("Unable get connection to " + url);
        }
    }


